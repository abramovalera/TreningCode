package HW.HW_Conditional_Operators.do_while;

public class OutputtingNumbers {
    public static void main(String[] args) {
        outputtingNumbers();
    }
    public static void outputtingNumbers(){

        int i = 1;
        do {
            System.out.println(i);
            i++;
        }while (i <=10);
    }
}
